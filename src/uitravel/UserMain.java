    private void loadHotelData(){
        pnlHotel.setLayout(new MigLayout("wrap,fill, insets 0","push[]10[]10[]10[]push","[]10[]"));

        allHotels = new ArrayList<>();
        for(int i=0;i<100;i++){
            Hotel t = new Hotel();
            t.setPreferredSize(new Dimension(300,350));
            pnlHotel.add(t);
            allHotels.add(t);
        }
        bg.add(pnlHotel);
    }
   
